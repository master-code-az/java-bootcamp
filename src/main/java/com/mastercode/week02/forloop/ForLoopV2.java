public class ForLoopV2 {


    public static void main(String[] args) {

        int i = 0; // tanitma

        for(        ;i < 5;       ) {
            System.out.println("Salam");
            i++;  // i = i + 1 -> i-ni bir vahid artirir  -> update
        }

    }

}
